#!/usr/bin/env bash
# agent-team 场景测试机器校验总入口（诚实复审 item 1）。
# 把场景测试的"通过"从 LLM 叙述改为退出码裁决。
#
# 用法:  bash pressure-test/verify/run-all.sh
# 退出码: 两层校验全过 → 0；任一失败 → 1
set -u
HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "════════════════════════════════════════════════════════"
echo " 第 1 层：交付物存在性 + development 真跑 vitest"
echo "════════════════════════════════════════════════════════"
python3 "$HERE/verify.py"
rc1=$?

echo "════════════════════════════════════════════════════════"
echo " 第 2 层：编排机制完整性（防叙述造假）"
echo "════════════════════════════════════════════════════════"
python3 "$HERE/verify_mechanism.py"
rc2=$?

echo "════════════════════════════════════════════════════════"
if [ "$rc1" -eq 0 ] && [ "$rc2" -eq 0 ]; then
  echo " 总结果: ✅ 两层全过（退出码 0）"
  exit 0
else
  echo " 总结果: ❌ 第1层=$rc1  第2层=$rc2（退出码 1）"
  echo " 说明: 当前已知 office 写前快照机制造假未修复 → 第2层预期失败。"
  echo "       这正是机器校验取代 LLM 叙述的意义：问题无处藏身。"
  exit 1
fi
